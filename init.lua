})

-- For Rust bracker auto completion
-- Configure nvim-autopairs
require('nvim-autopairs').setup{}

-- Rust specific autopairs configuration
local npairs = require('nvim-autopairs')
local Rule = require('nvim-autopairs.rule')

npairs.add_rules({
    Rule('{', '}', 'rust')
        :with_pair(function(opts)
            local _, _, col = vim.fn.getline('.'):find('{', opts.col)
            return col ~= nil
        end)
        :with_move(function(opts)
            local _, _, col = vim.fn.getline(opts.next_line):find('}', 1)
            return col ~= nil
        end)
        :use_key('}')
})

